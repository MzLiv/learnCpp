#include <iostream>

int main()
{
	// std::cout lives in the iostream library
	std::cout << "Hello, world!\n";
		
	// these comments can make the code hard to read
	std::cout << "It is very nice to meet you!\n";
	
	// especially when the lines are different lengths
	std::cout << "Yeah!\n";
	
	/*This is  multi-line comment.
	This line will be ignored.
	So will this one.
	* the asterisk will not be read by the program 
	* so it can be used for clarity*/
	return 0;
}